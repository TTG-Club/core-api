package club.ttg.dnd5.domain.revision.service;

/**
 * Обработчик отката для конкретного типа сущности.
 * <p>
 * Каждый домен (заклинания, существа, ...) регистрирует свой бин-обработчик,
 * который умеет применить сохранённый JSON-снимок к сущности — как правило,
 * десериализуя снимок в свой request-DTO и вызывая обычный метод обновления.
 * Так откат проходит ровно через тот же путь, что и ручное редактирование,
 * и сам порождает новую запись в истории.
 */
public interface RevisionRevertHandler {

    /** Тип сущности, который обслуживает обработчик (совпадает с entity_type). */
    String entityType();

    /**
     * Применяет снимок версии к текущей сущности.
     *
     * @param entityId     идентификатор сущности (url)
     * @param snapshotJson JSON-снимок из истории (форма редактора)
     */
    void revert(String entityId, String snapshotJson);
}
