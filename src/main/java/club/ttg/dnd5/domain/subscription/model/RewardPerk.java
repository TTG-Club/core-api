package club.ttg.dnd5.domain.subscription.model;

/**
 * Постоянная (одноразово выдаваемая) награда пользователя.
 * Награды не истекают и не путаются с временной подпиской.
 */
public enum RewardPerk {
    /** Ранний доступ к скачиванию. */
    EARLY_ACCESS_DOWNLOAD,
    /** Скачивание карты и токенов. */
    MAP_TOKENS_DOWNLOAD,
    /** Скачивание приключения (контент может быть готов позже). */
    ADVENTURE_DOWNLOAD,
    /** Доступ в приватный чат разработки. */
    DEV_CHAT_ACCESS,
    /** Уникальный значок рядом с именем. */
    PROFILE_BADGE,
    /** Уникальная рамка вокруг аватарки. */
    AVATAR_FRAME,
    /** Увековечивание в самом приложении. */
    APP_CREDITS
}
